const ROWS = MAP.length;
const COLS = MAP[0].length;

/**
 * Whether the given geographic point is land.
 *
 * @param latDeg - Latitude in degrees, +90 (north) … −90 (south).
 * @param lonDeg - Longitude in degrees, −180 … +180.
 */
export function isLand(latDeg: number, lonDeg: number): boolean {
  const u = (lonDeg + 180) / 360; // 0 … 1, west → east
  const v = (90 - latDeg) / 180; // 0 … 1, north → south
  const col = Math.min(COLS - 1, Math.max(0, Math.floor(u * COLS)));
  const row = Math.min(ROWS - 1, Math.max(0, Math.floor(v * ROWS)));
  return MAP[row][col] === "#";
}
